      methodChannelWithName:@"googlecloudspeech"
            binaryMessenger:[registrar messenger]];
  GooglecloudspeechPlugin* instance = [[GooglecloudspeechPlugin alloc] init];
  [registrar addMethodCallDelegate:instance channel:channel];
}

